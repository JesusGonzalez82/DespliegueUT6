/// Markdown es un lenguaje de marcado ligero muy utilizado para formatear texto en la web,
/// especialmente en documentación, blogs y README de repositorios en GitHub.
///
/// # Documentación oficial:
/// - Documentación oficial: [documentación oficial](https://markdown.es/)
/// - Hemos elegido la siguiente variante: [CommonMark](https://spec.commonmark.org/)
///
/// # Ventajas de Markdown:
/// - **Sintaxis sencilla y fácil de aprender**: Permite dar formato al texto sin necesidad de conocer HTML o CSS.
/// - *Ligero y rápido*: Al ser solo texto plano con marcas mínimas, es muy eficiente.
/// - `Compatible con múltiples plataformas`: Se usa en GitHub, foros, wikis y más.
/// - Exportable: Se puede convertir fácilmente a HTML, PDF y otros formatos.
/// - Legible incluso sin procesar: A diferencia de otros lenguajes de marcado, el texto sigue siendo comprensible en bruto.
///
/// # Desventajas de Markdown:
/// - Falta de estandarización: Existen varias versiones (CommonMark, GitHub Flavored Markdown, etc.), lo que puede causar inconsistencias.
/// - Funcionalidad limitada: No permite hacer cosas avanzadas como tablas dinámicas o estilos personalizados sin usar HTML/CSS.
/// - Soporte variable: Algunas plataformas no implementan todas las funciones de Markdown de la misma manera.
///
